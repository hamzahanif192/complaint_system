<script>
    let lastComplaintId = {{ $myComplaintMaxId ?? 0 }};

    setInterval(function() {
        $.ajax({
            url: '{{ route("fetch.new.complaints") }}',
            method: 'GET',
            data: { last_id: lastComplaintId },
            success: function (res) {
                if(res.html.trim() !== '') {
                    let tempDiv = $('<div>').html(res.html);
                    tempDiv.find('tr[id^="complaint-row-"]').each(function(){
                        let rowId = $(this).attr('id');
                        let existingRow = $('#' + rowId);
                        if (existingRow.length > 0) {
                            existingRow.replaceWith($(this)); // üîÑ Replace
                        } else {
                            $('#complaintsTableContainer tbody').append($(this)); // Nahi hai to add
                        }
                    });
                    lastComplaintId = res.last_id;
                }
            },
            error: function(){
                console.error('Error fetching complaints');
            }
        });
    }, 5000);
</script>



  <script>
    $(document).ready(function() {
      $('.submit-comment-btn').click(function() {
        let complaint_id = $(this).data('id');
        let comment = $('#comment-box-' + complaint_id).val();

        if (comment.trim() === '') return;

        $.ajax({
          url: '{{ route("store.tracking.comment") }}',
          type: 'POST',
          data: {
            _token: '{{ csrf_token() }}',
            complaint_id: complaint_id,
            comment: comment
          },
          success: function(res) {
            $('#comment-box-' + complaint_id).val('');
            $('#tracking-timeline-' + complaint_id).append(`
                <div class="mb-2 user_message">
